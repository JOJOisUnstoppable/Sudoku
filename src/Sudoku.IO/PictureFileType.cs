namespace Sudoku.IO
{
	/// <summary>
	/// Encapsulates a picture type.
	/// </summary>
	public enum PictureFileType : byte
	{
		/// <summary>
		/// Indicates the JPG format.
		/// </summary>
		Jpg,

		/// <summary>
		/// Indicates the PNG format.
		/// </summary>
		Png,

		/// <summary>
		/// Indicates the GIF format.
		/// </summary>
		Gif,

		/// <summary>
		/// Indicate the BMP format.
		/// </summary>
		Bmp
	}
}
